// Fonction pour obtenir une traduction
export const getTranslation = (key, language = 'fr', params = {}) => {
    try {
        const langTranslations = translations[language] || translations['fr'];
        let translation = langTranslations[key] || key;
        
        // Remplacer les paramètres dans la traduction
        Object.keys(params).forEach(param => {
            translation = translation.replace(`{${param}}`, params[param]);
        });
        
        return translation;
    } catch (error) {
        console.warn(`Translation error for key "${key}" in language "${language}":`, error);
        return key; // Retourner la clé si la traduction échoue
    }
};

// Fonction pour obtenir toutes les traductions d'une langue
export const getLanguageTranslations = (language = 'fr') => {
    return translations[language] || translations['fr'];
};
