// Fonction utilitaire pour obtenir une traduction avec interpolation
export const getTranslation = (key, language = 'fr', params = {}) => {
    const translation = translations[language]?.[key];
    
    if (!translation) {
        console.warn(`⚠️ Traduction manquante pour "${key}" en ${language}`);
        return key;
    }
    
    // Interpolation des paramètres
    let result = translation;
    Object.keys(params).forEach(param => {
        result = result.replace(`{${param}}`, params[param]);
    });
    
    return result;
};

// Fonction pour obtenir toutes les traductions d'une langue
export const getLanguageTranslations = (language = 'fr') => {
    return translations[language] || {};
};

// Fonction pour vérifier si une langue est supportée
export const isLanguageSupported = (language) => {
    return Object.keys(translations).includes(language);
};

// Fonction pour obtenir la liste des langues supportées
export const getSupportedLanguages = () => {
    return Object.keys(translations);
};
